weekDays = ["monday","tuesday","wednesday","Thursday","Friday",]
console.log("initially weekDays:",weekDays);


// insertion
// push method
weekDays.push("Saturday")
console.log("After using push method:",weekDays);

// unshift method
weekDays.unshift("Sunday")
console.log("After using unshift method:",weekDays);


// deletion
// pop method
weekDays.pop()
console.log("After using pop method:",weekDays);

// shift method
weekDays.shift()
console.log("After using shift method:",weekDays);