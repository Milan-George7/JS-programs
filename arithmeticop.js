number= 10
// division operator
console.log(`${number}/3 = ${number/3}`);
// modulus operator
console.log(`${number}%3 = ${number%3}`);
// Power operator
console.log(`${number}^3 = ${number**3}`);
// Subtraction operator
console.log(`${number}-3 = ${number-3}`);
// Addition operator
console.log(`${number}+3 = ${number+3}`);
// multiplication operator
console.log(`${number}*3 = ${number*3}`);
// increment/decrement operator
p=10
console.log(`${p}++ = ${++p} `);

q=10
console.log(`${q}-- = ${--q} `);


// short hand operators
r=20

console.log(`rx4 = ${r*=4}`);//r*=4 is same as r=r*4


s=30
console.log(`s+4 = ${s+=4}`);//s+=4 is same as s=s+4


t=25
console.log(`t-4 = ${t-=4}`);//t-=4 is same as t=t-4


v=40
console.log(`v/4 = ${v/=4}`);//v/=4 is same as v=v/4





